package group11.EventFiesta.EInvite;

public class EInviteModel {

    private String ceremonyName;
    private String eventHost1;
    private String eventHost2;
    private String venue;
    private String timeOfEvent;

    public String getCeremonyName() {
        return this.ceremonyName;
    }

    public void setCeremonyName(String ceremonyName) {
        this.ceremonyName = ceremonyName;
    }

    public String getEventHost1() {
        return this.eventHost1;
    }

    public void setEventHost1(String eventHost1) {
        this.eventHost1 = eventHost1;
    }

    public String getEventHost2() {
        return this.eventHost2;
    }

    public void setEventHost2(String eventHost2) {
        this.eventHost2 = eventHost2;
    }

    public String getVenue() {
        return this.venue;
    }

    public void setVenue(String venue) {
        this.venue = venue;
    }

    public String getTimeOfEvent() {
        return this.timeOfEvent;
    }

    public void setTimeOfEvent(String timeOfEvent) {
        this.timeOfEvent = timeOfEvent;
    }

}
